namespace BuddyLanguage.Domain.Enumerations;

public enum AudioFormat
{
    /// <summary>
    /// The Free Lossless Audio Codec (FLAC) is the most widely supported lossless audio codec.
    /// </summary>
    Flac,

    /// <summary>
    /// The MPEG-4 Part 14 or MP4 is a digital multimedia container format most commonly used to store video and audio, but it can also be used to store other data such as subtitles and still images.
    /// </summary>
    M4A,

    /// <summary>
    /// The MP3 or MPEG-1 Audio Layer III is a lossy audio format that is supported by most digital audio players.
    /// </summary>
    Mp3,

    /// <summary>
    /// The MPEG-4 Part 14 or MP4 is a digital multimedia container format most commonly used to store video and audio, but it can also be used to store other data such as subtitles and still images.
    /// </summary>
    Mp4,

    /// <summary>
    /// The Moving Picture Experts Group or MPEG is a working group of authorities that was formed by ISO and IEC to set standards for audio and video compression and transmission.
    /// </summary>
    Mpeg,

    /// <summary>
    /// The Moving Picture Experts Group or MPEG is a working group of authorities that was formed by ISO and IEC to set standards for audio and video compression and transmission.
    /// </summary>
    Mpga,

    /// <summary>
    /// The Ogg format is a free, open container format maintained by the Xiph.Org Foundation.
    /// </summary>
    Oga,

    /// <summary>
    /// The Ogg format is a free, open container format maintained by the Xiph.Org Foundation.
    /// </summary>
    Ogg,

    /// <summary>
    ///  The Waveform Audio File Format or WAV is a lossless audio format that is supported by most digital audio players.
    /// </summary>
    Wav,

    /// <summary>
    /// The WebM format is a free, open container format maintained by the Xiph.Org Foundation.
    /// </summary>
    Webm,
}
